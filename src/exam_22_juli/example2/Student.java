package exam_22_juli.example2;

import java.io.Serializable;
//Damit du ein Objekt speichern kannst, muss die Klasse Serializable implementieren:
// todo - can be edited, if needed.
public class Student implements Serializable {

    private String id;
    private String name;
    private String email;
    private static final long serialVersionUID = 1L; // empfohlen bei Serializable

    @Override
    public String toString() {
        return "Student{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", email='" + email + '\'' +
                '}';
    }

    public Student(String id, String name, String email) {
        this.id = id;
        this.name = name;
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}

